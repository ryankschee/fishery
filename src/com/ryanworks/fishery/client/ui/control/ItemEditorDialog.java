    private void jButtonOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOkActionPerformed
        try 
        {
            if (this.jTextFieldName.getText().trim().length() ==0)
            {
                this.jTextFieldName.requestFocus();
                this.jTextFieldName.selectAll();

                JOptionPane.showMessageDialog(
                        this, 
                        "Invalid 'Name' format. Please insert a valid name.", 
                        "Invalid Input", 
                        JOptionPane.ERROR_MESSAGE);
                
                return;
            }
            
            if (this.jTextFieldPrice.getText().trim().length() ==0)
            {
                this.jTextFieldPrice.requestFocus();
                this.jTextFieldPrice.selectAll();

                JOptionPane.showMessageDialog(
                        this, 
                        "Invalid 'Price' format. Please insert a valid price.", 
                        "Invalid Input", 
                        JOptionPane.ERROR_MESSAGE);
                
                return;
            }
            
            this.itemObj.setName(this.jTextFieldName.getText().trim());
            this.itemObj.setNameBm(this.jTextFieldNameBm.getText().trim());
            this.itemObj.setPrice(Double.parseDouble(this.jTextFieldPrice.getText().trim()));
            this.itemObj.setCategoryId(((CategoryBean)this.jComboBoxCategory.getSelectedItem()).getId());
            
            this.listener.itemEditorDialogClosed( this.itemObj );
            this.setVisible( false );
            this.dispose();
        }
        catch (NumberFormatException e)
        {
            this.jTextFieldPrice.requestFocus();
            this.jTextFieldPrice.selectAll();
            
            JOptionPane.showMessageDialog(
                    this, 
                    "Invalid 'Price' format. Must be decimal value.", 
                    "Invalid Input", 
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonOkActionPerformed

