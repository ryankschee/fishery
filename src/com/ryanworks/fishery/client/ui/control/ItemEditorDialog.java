    private void myInit()
    {
        if (this.itemObj == null)
        {
            this.editingMode = EDITOR_MODE_NEW;
            this.itemObj = new ItemBean();
        }
        else
        {
            this.editingMode = EDITOR_MODE_EDIT;
        }
        
        int selectedCategory = 0, i=0;
        List<CategoryBean> categoryList = SystemDataDelegate.getInstance().getAllCategories();
        for (CategoryBean category : categoryList)
        {
            this.jComboBoxCategory.addItem(category);
            if (category.getName().equals(itemCategory))
                selectedCategory = i;
            i++;
        }
        this.jComboBoxCategory.setSelectedIndex(selectedCategory);
        
        System.err.println("itemCategory="+itemCategory);
        System.err.println("selectedCategory="+selectedCategory);
        
        this.jTextFieldName.addFocusListener( new JTextFieldFocusListener() );
        this.jTextFieldNameBm.addFocusListener( new JTextFieldFocusListener() );
        this.jTextFieldPrice.addFocusListener( new JTextFieldFocusListener() );
        
        this.fillForm();
    }

    private void fillForm()
    {
        if (this.editingMode == ItemEditorDialog.EDITOR_MODE_NEW)
        {
            this.jTextFieldId.setText( this.itemObj.getId() );
            this.jComboBoxCategory.setSelectedIndex(0);
        }
        else
        if (this.editingMode == ItemEditorDialog.EDITOR_MODE_EDIT)
        {
            for (int i=0 ; i<this.jComboBoxCategory.getItemCount() ; i++)
            {
                if (((CategoryBean)this.jComboBoxCategory.getItemAt(i)).getId().equalsIgnoreCase( this.itemObj.getCategoryId() ) )
                {
                    this.jComboBoxCategory.setSelectedIndex(i);
                    break;
                }
            }
            
            this.jTextFieldId.setText( this.itemObj.getId() );
            this.jTextFieldName.setText( this.itemObj.getName() );
            this.jTextFieldNameBm.setText( this.itemObj.getNameBm() );
            this.jTextFieldPrice.setText( twoPlacesFormatter.format( this.itemObj.getPrice() ) );
        }
    }
    
