    private void myInit()
    {
        if (this.supplierObj == null)
        {
            this.editingMode = EDITOR_MODE_NEW;
            this.supplierObj = new SupplierBean();  
            this.jTextFieldId.addFocusListener( new JTextFieldFocusListener() );          
        }
        else
        {
            this.editingMode = EDITOR_MODE_EDIT;
        }
        
        
        this.jTextFieldName.addFocusListener( new JTextFieldFocusListener() );
        this.jTextFieldPercentage.addFocusListener( new JTextFieldFocusListener() );
        this.jTextAreaNotes.addFocusListener( new JTextAreaFocusListener() );
        
        this.fillForm();
    }

    private void fillForm()
    {
        if (this.editingMode == SupplierEditorDialog.EDITOR_MODE_NEW)
        {
            this.jTextFieldId.setEditable(true);
            this.jTextFieldId.setText( this.supplierObj.getId() );
            this.jTextFieldId.requestFocus();
            this.jTextFieldId.selectAll();
        }
        else
        if (this.editingMode == SupplierEditorDialog.EDITOR_MODE_EDIT)
        {
            this.jTextFieldId.setEditable(false);            
            this.jTextFieldId.setText( this.supplierObj.getId() );
            this.jTextFieldName.setText( this.supplierObj.getName() );
            this.jTextFieldNumber.setText( this.supplierObj.getShipNumber() );
            this.jCheckBoxFrequent.setSelected( this.supplierObj.isFrequent() );
            this.jCheckBoxSavingAccount.setSelected( this.supplierObj.isSavingAccount());
            this.jTextFieldPercentage.setText( twoPlacesFormatter.format( this.supplierObj.getPercentage()) );
            this.jTextAreaNotes.setText( this.supplierObj.getNotes() );
            
            this.jTextFieldName.requestFocus();
            this.jTextFieldName.selectAll();
        }
    }
    
