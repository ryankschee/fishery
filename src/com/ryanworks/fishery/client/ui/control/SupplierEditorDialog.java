    private void jButtonOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOkActionPerformed
        try 
        {
            if (this.jTextFieldName.getText().trim().length() ==0)
            {
                this.jTextFieldName.requestFocus();
                this.jTextFieldName.selectAll();

                JOptionPane.showMessageDialog(
                        this, 
                        "Invalid 'Name' format. Please insert a valid name.", 
                        "Invalid Input", 
                        JOptionPane.ERROR_MESSAGE);
                
                return;
            }
            
            if (this.jTextFieldPercentage.getText().trim().length() ==0)
            {
                this.jTextFieldPercentage.requestFocus();
                this.jTextFieldPercentage.selectAll();

                JOptionPane.showMessageDialog(
                        this, 
                        "Invalid 'Percentage' format. Please insert a valid percentage.", 
                        "Invalid Input", 
                        JOptionPane.ERROR_MESSAGE);
                
                return;
            }
            
            this.supplierObj.setName(this.jTextFieldName.getText().trim());
            this.supplierObj.setShipNumber(this.jTextFieldNumber.getText().trim());
            this.supplierObj.setFrequent(this.jCheckBoxFrequent.isSelected());
            this.supplierObj.setSavingAccount(this.jCheckBoxSavingAccount.isSelected());
            this.supplierObj.setPercentage(Double.parseDouble(this.jTextFieldPercentage.getText().trim()));
            this.supplierObj.setNotes(this.jTextAreaNotes.getText().trim());
            
            this.listener.supplierEditorDialogClosed( this.supplierObj );
            this.setVisible( false );
            this.dispose();
        }
        catch (NumberFormatException e)
        {
            this.jTextFieldPercentage.requestFocus();
            this.jTextFieldPercentage.selectAll();
            
            JOptionPane.showMessageDialog(
                    this, 
                    "Invalid 'Percentage' format. Must be decimal value.", 
                    "Invalid Input", 
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonOkActionPerformed

